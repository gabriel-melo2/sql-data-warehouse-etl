CREATE PROCEDURE USP_FULL_D_VENDEDOR

AS



IF OBJECT_ID('D_VENDEDOR') IS NULL 
BEGIN


--CRIA TABELA CASO NÃO EXISTA
CREATE TABLE D_VENDEDOR
(
  COD_VENDEDOR NUMERIC(15) PRIMARY KEY
, VENDEDOR VARCHAR(80)
)

END

--LIMPA DADOS DA DIMENSÃO
TRUNCATE TABLE D_VENDEDOR


--POPULA A DIMENSÃO
INSERT INTO D_VENDEDOR

SELECT *
  FROM PBS_PROCFIT_ST.DBO.VW_D_VENDEDORES